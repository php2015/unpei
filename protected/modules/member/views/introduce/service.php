<script>
$(function(){
    window.onscroll=function(){
        if(scroll==1)
            return false;
        var height=$(document).scrollTop();
        if(height<1000&&$(".nav ul li:eq(0)").hasClass('current')==false){
            $(".nav ul li").removeClass('current');
            $(".nav ul li:eq(0)").addClass('current');
        }
        else if(height>1000&&height<2400&&$(".nav ul li:eq(1)").hasClass('current')==false){
